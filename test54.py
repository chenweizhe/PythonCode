# 多任务:操作系统同时运行多个任务 一个任务相当于一个进程
# 一个进程又有多个子任务 即为线程
'''总结一下就是，多任务的实现有3种方式：
多进程模式；
多线程模式；
多进程+多线程模式。'''

# 线程是最小的执行单元，而进程由至少一个线程组成。如何调度进程和线程，完全由操作系统决定，程序自己不能决定什么时候执行，执行多长时间


